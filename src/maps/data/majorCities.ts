/**
 * The major-city set as a turf point array — used by the matching
 * Voronoi partition and the measuring "city" boundary so both build
 * their geometry from the SAME list the nearest-reference preview
 * shows. Building over the full worldwide set (not just cities inside
 * the play area, as the old Overpass query did) is also more correct:
 * the major city nearest a play-area point is frequently outside it.
 */
export function majorCityPoints(): GeoJSON.Feature<GeoJSON.Point>[] {
    return MAJOR_CITIES.map(([, lat, lng]) => turf.point([lng, lat]));
}
